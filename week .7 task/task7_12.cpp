#include<iostream>
using namespace std;
main()
{
   for( int rows=1;rows<=5;rows++)
    {
        for(int col=1;col<=rows;col++)
        {
            cout<<"*";
    }
    cout<<endl;
}
  for(int i=5;i>=1; i--)
        {
            for(int k=1;k<=i;k++)
            {
                cout<<"*";
            }
        cout<<endl;
    }
}
